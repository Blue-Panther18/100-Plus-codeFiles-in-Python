"""
This file performs simple arithmetic operations on two sets of integers.
"""

num1 = int(input("Enter the first integer: "))
num2 = int(input("Enter the second integer: "))

print(f"\nAddition of {num1} and {num2} is {num1 + num2}.")
print(f"Subtraction of {num2} from {num1} is {num1 - num2}.")
print(f"Multiplication of {num1} and {num2} is {num1 * num2}.")
print(f"Division of {num1} by {num2} is {num1 / num2}.\n")
